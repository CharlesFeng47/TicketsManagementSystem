package cn.edu.nju.charlesfeng.util.comparators;

import cn.edu.nju.charlesfeng.model.SeatInfo;

import java.util.Comparator;
import java.util.Map;

/**
 * 计划 Schedule 实体中对 Map<SeatInfo, Double> seatPrices 进行排序的比较器
 */
public class SeatPriceMapComparator implements Comparator<Map.Entry<SeatInfo, Double>> {
    @Override
    public int compare(Map.Entry<SeatInfo, Double> o1, Map.Entry<SeatInfo, Double> o2) {
        return (int) (o2.getValue() - o1.getValue());
    }
}
